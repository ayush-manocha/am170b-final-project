def plot_sweep_results(results, param_name):
    values = np.array([r[param_name] for r in results])
    lambdas = np.array([r["lambda_max"] for r in results])
    recalls = np.array([r["recall"] for r in results])
    precisions = np.array([r["precision"] for r in results])
    leads = np.array([r["mean_lead"] for r in results])
    r2_vals = np.array([r["short_r2_mean"] for r in results])

    fig, axes = plt.subplots(1, 4, figsize=(20, 4))

    axes[0].plot(values, lambdas, "o-", lw=2)
    axes[0].axhline(0, c="k", ls="--", lw=1)
    axes[0].set_xlabel(param_name)
    axes[0].set_ylabel(r"$\lambda_{max}$")
    axes[0].set_title("Chaos vs parameter")

    axes[1].plot(values, recalls, "o-", lw=2, label="Recall")
    axes[1].plot(values, precisions, "s--", lw=2, label="Precision")
    axes[1].set_xlabel(param_name)
    axes[1].set_ylabel("Score")
    axes[1].set_ylim(0, 1.05)
    axes[1].set_title("Burst prediction vs parameter")
    axes[1].legend()

    axes[2].scatter(lambdas, recalls, s=60, label="Recall")
    axes[2].scatter(lambdas, precisions, s=60, label="Precision")
    axes[2].axvline(0, c="k", ls="--", lw=1)
    axes[2].set_xlabel(r"$\lambda_{max}$")
    axes[2].set_ylabel("Score")
    axes[2].set_ylim(0, 1.05)
    axes[2].set_title("Burst prediction vs chaos")
    axes[2].legend()

    sc = axes[3].scatter(lambdas, r2_vals, c=values, s=80, edgecolor="k")
    axes[3].axvline(0, c="k", ls="--", lw=1)
    axes[3].set_xlabel(r"$\lambda_{max}$")
    axes[3].set_ylabel(r"Mean windowed $R^2$")
    axes[3].set_title("Short-term prediction vs chaos")
    cbar = plt.colorbar(sc, ax=axes[3])
    cbar.set_label(param_name)

    plt.tight_layout()
    plt.show()

    fig, axes = plt.subplots(1, 2, figsize=(12, 4))

    axes[0].plot(values, leads, "o-", lw=2)
    axes[0].set_xlabel(param_name)
    axes[0].set_ylabel("Mean lead time")
    axes[0].set_title("Prediction lead time vs parameter")

    sc = axes[1].scatter(lambdas, r2_vals, c=values, s=80, edgecolor="k")
    axes[1].axvline(0, c="k", ls="--", lw=1)
    axes[1].set_xlabel(r"$\lambda_{max}$")
